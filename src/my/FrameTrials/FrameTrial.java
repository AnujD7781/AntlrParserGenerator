    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
    FileNameExtensionFilter filter = new FileNameExtensionFilter( "java", "JAVA", "j");
    chooser.setFileFilter(filter);
    int returnVal = chooser.showOpenDialog(this);
    if(returnVal == JFileChooser.APPROVE_OPTION) {
       System.out.println("You chose to open this file: " +
            chooser.getSelectedFile().getPath());
       try {
                //init(chooser.getSelectedFile().getPath());
                ParserHelper helper = new ParserHelper();
                helper.initWithFileName(chooser.getSelectedFile().getPath());
                System.out.println(helper.getPackageName());
                DBManager manager = new DBManager ();
                manager.dbConn();
                manager.insertPackageName(helper);
                manager.insertClassValues(helper);
                //helper.tt(chooser.getSelectedFile().getPath());
                this.initializeComboz();
            } catch (Exception ex) {
                Logger.getLogger(FrameTrial.class.getName()).log(Level.SEVERE, null, ex);
            }
    }
    }//GEN-LAST:event_jButton1ActionPerformed

