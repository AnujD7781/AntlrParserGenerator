private ArrayList<ParserModel> getDataForPackage(String selectedPackage) throws SQLException, IOException {
        DBManager manager = new DBManager();
        manager.dbConn();
        ParserHelper helper = null ;
        
        ArrayList<ParserHelper> arrHelper = manager.getPackageData(selectedPackage);
        if (arrHelper.size() > 0){
            helper =  arrHelper.get(0);
        }
        
        return helper.getDataForPackage();
        
    }
